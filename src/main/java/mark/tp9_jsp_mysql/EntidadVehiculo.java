
package mark.tp9_jsp_mysql;


public class EntidadVehiculo {
    
    
    int id_vehiculo;
    String marca;
    String modelo;
    String aniofabricacion;
    String patente;
    String color;
    String tipotransmision;
    int cantplazas;
    int capacidadbaul;
    int pma;

    
    public int getId_vehiculo() {
        return id_vehiculo;
    }

    public void setId_vehiculo(int id_vehiculo) {
        this.id_vehiculo = id_vehiculo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getAniofabricacion() {
        return aniofabricacion;
    }

    public void setAniofabricacion(String aniofabricacion) {
        this.aniofabricacion = aniofabricacion;
    }

    public String getPatente() {
        return patente;
    }

    public void setPatente(String patente) {
        this.patente = patente;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getTipotransmision() {
        return tipotransmision;
    }

    public void setTipotransmision(String tipotransmision) {
        this.tipotransmision = tipotransmision;
    }

    public int getCantplazas() {
        return cantplazas;
    }

    public void setCantplazas(int cantplazas) {
        this.cantplazas = cantplazas;
    }

    public int getCapacidadbaul() {
        return capacidadbaul;
    }

    public void setCapacidadbaul(int capacidadbaul) {
        this.capacidadbaul = capacidadbaul;
    }

    public int getPma() {
        return pma;
    }

    public void setPma(int pma) {
        this.pma = pma;
    }

            
    
}
